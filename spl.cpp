/*Share between Embed & Full*/
#include "magik.h"

const char* sheet[8]={"c4","g4","e4","c5","e5","g5","c6","c6"};
uint8_t q=0;
uint32_t next_note=0;
bool graphics::splash(bool enable){
	static bool flick=false;
	if(!enable){if(!flick){iproc.offtime=iproc.time;flick=true;}return true;}
	if(iproc.time/1000<4){
		/*Edit in this scope only...*/
		static uint8_t crow[4][8]={{0x00,0x00,0x00,0x00,0x0F,0x10,0x24,0x40},{0x00,0x00,0x00,0x00,0x00,0x80,0x80,0x40},{0x30,0x10,0x22,0x22,0x11,0x0C,0x03,0x06},{0x20,0x20,0x10,0x10,0x08,0x9C,0x60,0xC0}};
		static uint8_t andrew[12][8]={{0x00,0x00,0x00,0x00,0x3B,0x3F,0x7E,0x60},{0x00,0x00,0x00,0x1F,0xFF,0xF8,0x00,0x00},{0x01,0x0F,0xFF,0xFC,0x80,0x00,0x00,0x00},{0xF0,0xF0,0xB8,0x3C,0x1C,0x0C,0x0C,0x0C},{0x61,0x63,0x63,0x60,0x60,0x67,0x67,0x6E},{0xF0,0xFC,0xBC,0x0C,0xC0,0xF8,0xFC,0x7D},{0x00,0x00,0x00,0x00,0x00,0x78,0xFE,0xDF},{0x1C,0x3C,0x7C,0x7E,0x0E,0x06,0x06,0x06},{0x6C,0x6C,0x6C,0x6C,0x7F,0xFF,0xC3,0xC0},{0x7D,0x7F,0x7F,0x7F,0x7F,0xFD,0xFD,0xF0},{0x9F,0x9F,0x9F,0x1F,0x9F,0xFF,0xFF,0x7E},{0x0E,0x7E,0x7C,0x70,0xFC,0xFF,0x8F,0x01}};

		gfx.clear(15);
		int16_t x=(gfx.width/4)*3;
		int16_t y=gfx.height-(8*2);
		for(int i=0;i<4;i++)gfx.cell(crow[i],(i%2)*8+x,(i/2)*8+y,0);

		x=(gfx.width/4);y=gfx.height-(8*3);
		for(int i=0;i<12;i++)gfx.cell(andrew[i],(i%4)*8+x,(i/4)*8+y,0);

		gfx.setFont("6x12");const char* ss="Croviate64";
		uint16_t w=gfx.fontWidth();uint16_t h=gfx.fontHeight();
		uint8_t s=strlen(ss);
		for(int i=0;i<s;i++){
			int x=i*w+(gfx.width-(w*s))/2;
			int y=(gfx.height-h)/2+sin(iproc.time/100.0f+i/2.0f)*(h/4);
			gfx.glyph(ss[i],x,y,0);
		}
		uint32_t now=iproc.time;
		if(q<8&&now>=next_note){
			snd.tone(snd.noteFrequency[snd.noteIndex(sheet[q])]);
			q++;next_note=now+200;
		}
		if (q>=8)snd.noTone();
	}else{if(!flick){iproc.offtime=iproc.time;flick=true;}return true;}
	gfx.paste();
	return false;

}